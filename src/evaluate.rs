#[inline(always)]
pub fn piece_value(piece: Piece) -> i32 {
    match piece {
        Piece::King => 20000,
        Piece::Queen => 900,
        Piece::Rook => 500,
        Piece::Bishop => 330,
        Piece::Knight => 320,
        Piece::Pawn => 100,
    }
}

